package com.dante.learn.core.oop.basic;

/**
 * Instantiate variable and class varable will be clear with default value
 * Local variable is not
 */
public class TestDefaultInitialise {
	private String a;
	private int b;
	public TestDefaultInitialise() {
		
	}
	public String getA() {
		return a;
	}
	public void setA(String a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	
}
